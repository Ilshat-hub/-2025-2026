# Задача 7: Подмножества
# Даны два множества:
# A = {1, 2, 3}
# B = {1, 2, 3, 4, 5}
# Проверьте, является ли A подмножеством B и является ли B надмножеством A.
# (использовать: issubset(), issuperset() или <=, >=)
A = {1, 2, 3}
B = {1, 2, 3, 4, 5}

print("Множество A:", A)
print("Множество B:", B)
print("A является подмножеством B?", A.issubset(B))
print("B является надмножеством A?", B.issuperset(A))
print("A <= B?", A <= B)
print("B >= A?", B >= A)

# Задача 8: Обработка данных
# Дан список email-адресов пользователей, некоторые из которых повторяются:
# ["user1@mail.com", "user2@mail.com", "user1@mail.com", "user3@mail.com", "user2@mail.com"]
# Создайте множество уникальных адресов, добавьте новый адрес "user4@mail.com", затем преобразуйте множество обратно в список.
# (использовать: set(), add(), list())
emails = ["user1@mail.com", "user2@mail.com", "user1@mail.com", "user3@mail.com", "user2@mail.com"]
print("Исходный список:", emails)

unique_emails = set(emails)
print("Уникальные адреса:", unique_emails)

unique_emails.add("user4@mail.com")
print("После добавления user4@mail.com:", unique_emails)

emails_clean = list(unique_emails)
print("Очищенный список:", emails_clean)

# Задача 9: Поэтапная работа с множеством
# Создайте множество чисел от 1 до 10. Затем:
#
# Удалите все четные числа
# Добавьте числа 11, 12, 13
# Удалите числа меньше 5
# Проверьте, есть ли в множестве числа 7 и 15
# (использовать: set(), remove()/discard(), add(), in)

print("\n=== Задача 9 ===")
numbers = set(range(1, 11))
print("Исходное множество:", numbers)

numbers_to_remove = {x for x in numbers if x % 2 == 0}
for num in numbers_to_remove:
    numbers.discard(num)
print("После удаления четных чисел:", numbers)

numbers.add(11)
numbers.add(12)
numbers.add(13)
print("После добавления 11, 12, 13:", numbers)

numbers_to_remove = {x for x in numbers if x < 5}
for num in numbers_to_remove:
    numbers.discard(num)
print("После удаления чисел < 5:", numbers)

print("Есть ли число 7?", 7 in numbers)
print("Есть ли число 15?", 15 in numbers)