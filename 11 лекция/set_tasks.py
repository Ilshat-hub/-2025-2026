# Задачи на работу с множествами
#
# Задача 1: Уникальные элементы списка
# Дан список: [5, 2, 8, 2, 5, 1, 8, 8, 3]. Создайте из него множество, чтобы удалить дубликаты,
# и определите количество уникальных элементов.
# (использовать: set(), len())
sp = [5, 2, 8, 2, 5, 1, 8, 8, 3]
sp1 = set(sp)
print(len(sp) - len(sp1))
#
# Задача 2: Общие интересы
# У двух друзей есть списки любимых фильмов:
#
# Вася: ["Матрица", "Начало", "Матрица", "Интерстеллар"]
# Петя: ["Начало", "Аватар", "Интерстеллар", "Титаник"]
# Найдите фильмы, которые нравятся обоим друзьям.
# (использовать: intersection() или &)
vs = ["Матрица", "Начало", "Матрица", "Интерстеллар"]
pt = ["Начало", "Аватар", "Интерстеллар", "Титаник"]
vs1 = set(vs)
pt1 = set(pt)
obs = vs1 & pt1
print(obs)
# Задача 3: Уникальные предпочтения
# Используя данные из Задачи 2, найдите фильмы, которые есть только у Васи, но нет у Пети.
# (использовать: difference() или -)
obs1 = vs1 - pt1
print(obs1)
#
# Задача 4: Все различные фильмы
# Найдите все различные фильмы, которые есть у Васи и Пети вместе (без повторений).
# (использовать: union() или |)
obs2= vs1 | pt1
print(obs2)
#
# Задача 5: Разные вкусы
# Найдите фильмы, которые есть только у одного из друзей (либо только у Васи, либо только у Пети).
# (использовать: symmetric_difference() или ^)
obs3= vs1 ^ pt1
print(obs3)
