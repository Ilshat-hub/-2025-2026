# Задача 6: Работа с элементами множества
# Создайте пустое множество fruits. Добавьте в него "яблоко", "банан", "апельсин".
# Затем удалите "банан" и попробуйте удалить "груша" (без ошибки). Проверьте, есть ли "апельсин" в множестве.
# (использовать: add(), remove()/discard(), in)
fruits = set()
fruits.add("яблоко")
fruits.add("банан")
fruits.add("апельсин")
print(fruits)

fruits.remove("банан")
print(fruits)

fruits.discard("груша")
print( fruits)

print("Есть ли 'апельсин' в множестве?", "апельсин" in fruits)
print("Есть ли 'банан' в множестве?", "банан" in fruits)
