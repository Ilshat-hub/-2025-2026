# ------------------------------
# –ú–µ—Ç–æ–¥ union() –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç –¥–≤–∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–æ–≤–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ:
users = {"Tom", "Bob", "Alice"}
users2 = {"Sam", "Kate", "Bob"}

users3 = users.union(users2)
print(users3)  # {"Bob", "Alice", "Sam", "Kate", "Tom"}

# –í–º–µ—Å—Ç–æ –º–µ—Ç–æ–¥–∞ union() –º—ã –º–æ–≥–ª–∏ –±—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –æ–ø–µ—Ä–∞—Ü–∏—é –ª–æ–≥–∏—á–µ—Å–∫–æ–≥–æ —Å–ª–æ–∂–µ–Ω–∏—è - |:
set3 = {1, 2, 3}
set4 = {3, 4, 5}
union_set = set3 | set4  # –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ "|"
print("–û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ set3 –∏ set4:", union_set)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 2, 3, 4, 5}

# ------------------------------
# –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ (intersection)
# ------------------------------
# –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–ª—É—á–∏—Ç—å —Ç–æ–ª—å–∫–æ —Ç–µ —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ –≤ –æ–±–æ–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤–∞—Ö.
# –ú–µ—Ç–æ–¥ intersection() –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç –æ–ø–µ—Ä–∞—Ü–∏—é –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –º–Ω–æ–∂–µ—Å—Ç–≤ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–æ–≤–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ:
users = {"Tom", "Bob", "Alice"}
users2 = {"Sam", "Kate", "Bob"}

users3 = users.intersection(users2)
print(users3)  # {"Bob"}

# –í–º–µ—Å—Ç–æ –º–µ—Ç–æ–¥–∞ intersection –º—ã –º–æ–≥–ª–∏ –±—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –æ–ø–µ—Ä–∞—Ü–∏—é –ª–æ–≥–∏—á–µ—Å–∫–æ–≥–æ —É–º–Ω–æ–∂–µ–Ω–∏—è:
intersection_set = set3 & set4  # –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ "&"
print("–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ set3 –∏ set4:", intersection_set)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {3}

# ------------------------------
# –†–∞–∑–Ω–æ—Å—Ç—å (difference)
# ------------------------------
# –†–∞–∑–Ω–æ—Å—Ç—å –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ –ø–µ—Ä–≤–æ–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ, –Ω–æ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤–æ –≤—Ç–æ—Ä–æ–º.
# –î–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–∞–∑–Ω–æ—Å—Ç–∏ –º–Ω–æ–∂–µ—Å—Ç–≤ –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –º–µ—Ç–æ–¥ difference –∏–ª–∏ –æ–ø–µ—Ä–∞—Ü–∏—é –≤—ã—á–∏—Ç–∞–Ω–∏—è:
users = {"Tom", "Bob", "Alice"}
users2 = {"Sam", "Kate", "Bob"}

users3 = users.difference(users2)
print(users3)  # {"Tom", "Alice"}
print(users - users2)  # {"Tom", "Alice"}

difference_set = set3 - set4  # –≠–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ set3, –Ω–æ –Ω–µ—Ç –≤ set4
print("–†–∞–∑–Ω–æ—Å—Ç—å –º–Ω–æ–∂–µ—Å—Ç–≤ set3 –∏ set4:", difference_set)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 2}

# ------------------------------
# –°–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å (symmetric difference)
# ------------------------------
# –°–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –æ–±–æ–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤, –∫—Ä–æ–º–µ –æ–±—â–∏—Ö.
# —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ symmetric_difference() –∏–ª–∏ —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ü–∏–∏ ^.
users = {"Tom", "Bob", "Alice"}
users2 = {"Sam", "Kate", "Bob"}

users3 = users.symmetric_difference(users2)
print(users3)  # {"Tom", "Alice", "Sam", "Kate"}

users4 = users ^ users2
print(users4)  # {"Tom", "Alice", "Sam", "Kate"}

symmetric_difference_set = set3 ^ set4  # –°–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ "^"
print("–°–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å –º–Ω–æ–∂–µ—Å—Ç–≤ set3 –∏ set4:", symmetric_difference_set)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 2, 4, 5}


# ==========================================================
# ‚ûï –î–û–ë–ê–í–õ–ï–ù–ò–ï –ò ‚ûñ –£–î–ê–õ–ï–ù–ò–ï –≠–õ–ï–ú–ï–ù–¢–û–í
# ==========================================================

set5 = {1, 2, 3}
set5.add(4)  # –î–æ–±–∞–≤–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç 4 –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ
print("–ü–æ—Å–ª–µ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è —ç–ª–µ–º–µ–Ω—Ç–∞ 4:", set5)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 2, 3, 4}

set5.remove(2)  # –£–¥–∞–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç 2 –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞
print("–ü–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è —ç–ª–µ–º–µ–Ω—Ç–∞ 2:", set5)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 3, 4}

# –ï—Å–ª–∏ –º—ã –ø—ã—Ç–∞–µ–º—Å—è —É–¥–∞–ª–∏—Ç—å —ç–ª–µ–º–µ–Ω—Ç, –∫–æ—Ç–æ—Ä–æ–≥–æ –Ω–µ—Ç –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ, –≤–æ–∑–Ω–∏–∫–Ω–µ—Ç –æ—à–∏–±–∫–∞ KeyError:
# set5.remove(10)  # –≠—Ç–æ –≤—ã–∑–æ–≤–µ—Ç –æ—à–∏–±–∫—É, —Ç–∞–∫ –∫–∞–∫ 10 –Ω–µ—Ç –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ

# –ü–æ—ç—Ç–æ–º—É –ø–µ—Ä–µ–¥ —É–¥–∞–ª–µ–Ω–∏–µ–º —Å–ª–µ–¥—É–µ—Ç –ø—Ä–æ–≤–µ—Ä—è—Ç—å –Ω–∞ –Ω–∞–ª–∏—á–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ in:
users = {"Tom", "Bob", "Alice"}

user = "Tom"
if user in users:
    users.remove(user)
print(users)  # {"Bob", "Alice"}

# –î–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ —É–¥–∞–ª–µ–Ω–∏—è –±–µ–∑ –æ—à–∏–±–∫–∏ –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –º–µ—Ç–æ–¥ discard
set5.discard(10)  # –≠–ª–µ–º–µ–Ω—Ç 10 –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –Ω–æ –æ—à–∏–±–∫–∞ –Ω–µ –≤–æ–∑–Ω–∏–∫–Ω–µ—Ç
print("–ü–æ—Å–ª–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è discard –¥–ª—è –Ω–µ—Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞:", set5)

# –î–ª—è —É–¥–∞–ª–µ–Ω–∏—è –≤—Å–µ—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –º–µ—Ç–æ–¥ clear():
set5.clear()  # –û—á–∏—â–∞–µ–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞
print("–ü–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ set5:", set5)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: set()


# ==========================================================
# ‚úÖ –ü–†–û–í–ï–†–ö–ê –ü–†–ò–ù–ê–î–õ–ï–ñ–ù–û–°–¢–ò –ò –°–†–ê–í–ù–ï–ù–ò–ï –ú–ù–û–ñ–ï–°–¢–í
# ==========================================================

set6 = {1, 2, 3, 4, 5}
print("–ü—Ä–æ–≤–µ—Ä–∫–∞, –µ—Å—Ç—å –ª–∏ 3 –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ set6:", 3 in set6)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: True
print("–ü—Ä–æ–≤–µ—Ä–∫–∞, –µ—Å—Ç—å –ª–∏ 10 –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ set6:", 10 in set6)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: False

# –ú–Ω–æ–∂–µ—Å—Ç–≤–∞ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—Ç —Ç–∞–∫–∂–µ –æ–ø–µ—Ä–∞—Ü–∏–∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
# –ü—Ä–æ–≤–µ—Ä–∏–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –æ–¥–Ω–æ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –ø–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –¥—Ä—É–≥–æ–≥–æ
set7 = {1, 2}
set8 = {1, 2, 3, 4}
print("–ú–Ω–æ–∂–µ—Å—Ç–≤–æ set7 —è–≤–ª—è–µ—Ç—Å—è –ø–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –º–Ω–æ–∂–µ—Å—Ç–≤–∞ set8:", set7 <= set8)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: True

# –ü—Ä–æ–≤–µ—Ä–∏–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –æ–¥–Ω–æ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –Ω–∞–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –¥—Ä—É–≥–æ–≥–æ
print("–ú–Ω–æ–∂–µ—Å—Ç–≤–æ set8 —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –º–Ω–æ–∂–µ—Å—Ç–≤–∞ set7:", set8 >= set7)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: True


# ==========================================================
# üîÅ –ò–¢–ï–†–ê–¶–ò–ò –ò –ü–†–ï–û–ë–†–ê–ó–û–í–ê–ù–ò–Ø
# ==========================================================

# –ú–Ω–æ–∂–µ—Å—Ç–≤–∞ –º–æ–∂–Ω–æ –∏—Ç–µ—Ä–∏—Ä–æ–≤–∞—Ç—å –≤ —Ü–∏–∫–ª–µ
for elem in set8:
    print(elem, end=" ")  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: 1 2 3 4

# –ú–Ω–æ–∂–µ—Å—Ç–≤–∞ –º–æ–≥—É—Ç –±—ã—Ç—å –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω—ã –≤ –¥—Ä—É–≥–∏–µ –∫–æ–ª–ª–µ–∫—Ü–∏–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä, –≤ —Å–ø–∏—Å–æ–∫
list_from_set = list(set8)
print("\n–°–ø–∏—Å–æ–∫ –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ set8:", list_from_set)  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: [1, 2, 3, 4]

# –ú–æ–∂–Ω–æ –ø—Ä–æ–≤–µ—Ä—è—Ç—å, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –ø–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –¥—Ä—É–≥–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞
# –ú–µ—Ç–æ–¥ issubset –ø–æ–∑–≤–æ–ª—è–µ—Ç –≤—ã—è—Å–Ω–∏—Ç—å, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—É—â–µ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –ø–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º (—Ç–æ –µ—Å—Ç—å —á–∞—Å—Ç—å—é) –¥—Ä—É–≥–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞:
print("set7 —è–≤–ª—è–µ—Ç—Å—è –ø–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º set8:", set7.issubset(set8))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: True

# –ú–æ–∂–Ω–æ –ø—Ä–æ–≤–µ—Ä—è—Ç—å, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –Ω–∞–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º –¥—Ä—É–≥–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞
# –ú–µ—Ç–æ–¥ issuperset, –Ω–∞–æ–±–æ—Ä–æ—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True, –µ—Å–ª–∏ —Ç–µ–∫—É—â–µ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º (—Ç–æ –µ—Å—Ç—å —Å–æ–¥–µ—Ä–∂–∏—Ç) –¥–ª—è –¥—Ä—É–≥–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞:
print("set8 —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ–º set7:", set8.issuperset(set7))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: True

# –ú–Ω–æ–∂–µ—Å—Ç–≤–æ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ–º –¥—Ä—É–≥–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤
print("–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ set7 –∏ set8:", set7.intersection(set8))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: {1, 2}



