# Задача 8: Практическое применение
# Создайте кортеж для хранения координат точек на плоскости:
#
# pointA = (3, 4)
# pointB = (7, 1)
# Напишите функцию, которая вычисляет расстояние между двумя точками по формуле: √((x2-x1)² + (y2-y1)²)
# Для вычисления квадратного корня можно использовать функцию sqrt из модуля math
# (использовать: кортежи как неизменяемые координаты, math.sqrt)
print("\n=== Задача 8 ===")
import math

pointA = (3, 4)
pointB = (7, 1)

def distance(p1, p2):
    return math.sqrt((p2[0] - p1[0])**2 + (p2[1] - p1[1])**2)

dist = distance(pointA, pointB)
print(f"Точка A: {pointA}")
print(f"Точка B: {pointB}")
print(f"Расстояние между точками: {dist:.2f}")