# Индексация начинается с 0 (первый элемент имеет индекс 0)
fruits = ['яблоко', 'банан', 'апельсин', 'киви', 'манго']

# Прямая индексация (слева направо)
print(fruits[0])   # 'яблоко' - первый элемент
print(fruits[1])   # 'банан'  - второй элемент
print(fruits[2])   # 'апельсин' - третий элемент

# Отрицательная индексация (справа налево)
print(fruits[-1])  # 'манго'   - последний элемент
print(fruits[-2])  # 'киви'    - предпоследний элемент
print(fruits[-3])  # 'апельсин' - третий с конца

# Изменение элемента по индексу
fruits[1] = 'груша'
print(fruits)  # ['яблоко', 'груша', 'апельсин', 'киви', 'манго']

