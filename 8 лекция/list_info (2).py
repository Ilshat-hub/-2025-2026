# Описание: Пример использования всех основных методов работы со списками в Python

# Список (list) — это изменяемая упорядоченная структура данных, которая хранит элементы в определённом порядке.
# Элементы могут быть любого типа (числа, строки, другие списки и т.д.),
# доступ к элементам осуществляется по индексу (порядковому номеру).

# Используй СПИСОК для:
#    - Очереди задач
#    - Истории действий
#    - Рейтингов и топов
#    - Любых последовательностей
#    - Когда важен порядок

